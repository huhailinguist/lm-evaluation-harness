import yaml


all_subtasks = [
    "Anaphora",
    "ArgumentStructure", 
    "Classifier",
    "CommonSense",
    "Comparative",
    "DoubleNegation",
    "Idioms",
    "LexicalSemantics",
    "Monotonicity",
    "Negation",
    "NonCoreArguments",
    "ProDrop",
    "TimeOfEvent",
    "WorldKnowledge"
]


def main() -> None:
    for task in all_subtasks:
        file_name = f"{task}.yaml"
        try:
            with open(f"{file_name}", "w", encoding="utf-8") as f:
                f.write("# Generated by generate_configs.py\n")
                yaml.dump(
                    {
                        "include": "_template_yaml",
                        "task": "ChineseNLIDiagnostics_" + task,
                        "dataset_name": task,
                    },
                    f,
                )
        except FileExistsError:
            pass


if __name__ == "__main__":
    main()